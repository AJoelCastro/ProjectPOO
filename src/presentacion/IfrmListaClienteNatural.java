    private void llenarTabla(){
        modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(columnas);
        for(int i=0;i<lista.getTamanio();i++){
            clienteN = (ClienteNatural)lista.obtenerCliente(i);
            fila[0] = clienteN.getIdentificacion();
            fila[1] = clienteN.getNombre();
            fila[2] = clienteN.getApellido();
            modelo.addRow(fila);
        }
        tblPersonas.setModel(modelo);
    }
